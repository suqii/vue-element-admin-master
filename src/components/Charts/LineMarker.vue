      charts().then(response => {
        // console.log(response.data.wordCloud)
        this.lineMarkerData = response.data.line
        this.initChart()
      })
    },
    initChart() {
      this.chart = echarts.init(document.getElementById(this.id))

      this.chart.setOption({
        backgroundColor: '#394056',
        title: {
          top: 20,
          text: '用户量增加状况',
          textStyle: {
